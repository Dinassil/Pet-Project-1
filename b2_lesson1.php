        <h1>Тема: Владение и Применение Притяжательных и Составных Существительных в Английском Языке</h1>

<h2>Введение:</h2>
<p>В данном сочинении мы рассмотрим важные аспекты английской грамматики, а именно, притяжательные и составные существительные. Эти концепции играют ключевую роль в формировании языка и позволяют точно выражать владение и связанные с этим понятия. Мы исследуем их структуру, применение и примеры использования в контексте.</p>

<h2>Притяжательные Существительные:</h2>
<p>Притяжательные существительные показывают принадлежность чего-то или кого-то. Они образуются с помощью апострофа и добавления 's в конце слова. Например, в предложении "дом Шейлы красив", "Шейла" является притяжательным существительным, обозначающим владение. Это важно учитывать при формировании собственных словосочетаний.</p>

<h2>Примеры использования притяжательных существительных:</h2>
<ul>
    <li>"Дом Шейлы" (Sheila's house)</li>
    <li>"Брат Габриеля" (Gabriel's brother)</li>
    <li>"Комната Джонсов" (the Jones's room)</li>
</ul>

<h2>Составные Существительные:</h2>
<p>Составные существительные состоят из двух или более слов, объединенных в одно. Они могут быть написаны как одно слово, через дефис или раздельно. Например, "книжный магазин", "бейсбольный мяч", "поезд на рельсах" - все это примеры составных существительных. Они расширяют наш словарный запас и делают язык более точным.</p>

<h2>Примеры составных существительных:</h2>
<ul>
    <li>"Книготорговец" (bookstore)</li>
    <li>"Подъезд" (crosswalk)</li>
    <li>"Лунный свет" (moonlight)</li>
</ul>

